    char interpret(int x)
    {
        char c='a';
        for(int i=0;i<=26;i++)
        {
            if((x)==i)
            {
                c=(char)(c+i);
            }
        }
        return c ;
    }
    int reinterpret(char c)
    {
        int x=0;
        char y='a';
        for(int i=0;i<=26;i++)
        {
            if(c==(char)(y+i))
            {
                x=i;
            }
        }
        return x;
    }
    int modulo(int x,int mod)
    {
        if(x>mod)
        {
            x=x%mod;
        }
        else if(x<0)
        {
            while(x<0)
                x+=mod;
        }
        return x;
    }
